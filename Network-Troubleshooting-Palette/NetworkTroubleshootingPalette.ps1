    switch ($selection) {
        '1' { Test-Connectivity }
        '2' { Trace-Route }
        '3' { DNS-Lookup }
        '4' { Test-Port }
        '5' { Show-Connections }
        '6' { Show-AdapterInfo }
        '7' { Show-Routing }
        '8' { Get-PublicIP }
        '9' { Get-WifiSignal }
        '10' { Test-SSL }
        '11' { Get-MTU }
        '12' { Invoke-PortScan }
        'L' { Toggle-Logging }
        'l' { Toggle-Logging }
        'Q' { 
            if ($script:LoggingEnabled) { Stop-Transcript }
            Write-Host "Exiting..."
            return
        }
        'q' { 
            if ($script:LoggingEnabled) { Stop-Transcript }
            Write-Host "Exiting..."
            return
        }
        default { 
            Write-Warning "Invalid selection. Please try again." 
            Start-Sleep -Seconds 1
        }
    }
} while ($true)
